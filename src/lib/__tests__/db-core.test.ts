// validateRequiredEnv
// ────────────────────────────────────────────────────────────────────────────
describe('validateRequiredEnv', () => {
  const origEnv = { ...process.env }

  afterEach(() => {
    process.env = { ...origEnv }
    vi.clearAllMocks()
  })

  it('warns when neither AUTH_PASS nor AUTH_PASS_B64 is set', () => {
    process.env.AUTH_PASS = ''
    process.env.AUTH_PASS_B64 = ''
    validateRequiredEnv()
    expect(mockLogger.warn).toHaveBeenCalledWith(
      expect.stringContaining('AUTH_PASS')
    )
  })

  it('does not warn when AUTH_PASS is set', () => {
    process.env.AUTH_PASS = 'strong-password-here'
    process.env.AUTH_PASS_B64 = ''
    validateRequiredEnv()
    // warn may still be called for other reasons but not for missing auth pass
    const calls: string[] = mockLogger.warn.mock.calls.map((c: any[]) => String(c[0]))
    const authWarn = calls.some((msg) => msg.includes('Neither AUTH_PASS'))
    expect(authWarn).toBe(false)
  })

  it('does not warn when AUTH_PASS_B64 is set', () => {
    process.env.AUTH_PASS = ''
    process.env.AUTH_PASS_B64 = Buffer.from('secure-pass').toString('base64')
    validateRequiredEnv()
    const calls: string[] = mockLogger.warn.mock.calls.map((c: any[]) => String(c[0]))
    const authWarn = calls.some((msg) => msg.includes('Neither AUTH_PASS'))
    expect(authWarn).toBe(false)
  })

  it('warns when MISSION_CONTROL_DB_PATH directory does not exist', () => {
    process.env.MISSION_CONTROL_DB_PATH = '/absolutely/nonexistent/path/db.sqlite'
    validateRequiredEnv()
    expect(mockLogger.warn).toHaveBeenCalledWith(
      expect.stringContaining('does not exist')
    )
  })
})

