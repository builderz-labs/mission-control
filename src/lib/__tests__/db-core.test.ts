// ────────────────────────────────────────────────────────────────────────────
// resolveSeedAuthPassword — additional edge cases beyond db-seed-auth-pass.test
// ────────────────────────────────────────────────────────────────────────────
describe('resolveSeedAuthPassword — edge cases', () => {
  it('returns null when AUTH_PASS_B64 is whitespace only', () => {
    const result = resolveSeedAuthPassword({
      AUTH_PASS_B64: '   ',
    } as unknown as NodeJS.ProcessEnv)
    expect(result).toBeNull()
  })

  it('falls back to AUTH_PASS when AUTH_PASS_B64 decodes to empty string', () => {
    // An empty string base64-encodes to "" which has 0 length after decode
    const emptyB64 = Buffer.from('', 'utf8').toString('base64') // ""
    const result = resolveSeedAuthPassword({
      AUTH_PASS_B64: emptyB64,
      AUTH_PASS: 'fallback',
    } as unknown as NodeJS.ProcessEnv)
    // Empty encoded string → decoded is empty → falls through to AUTH_PASS
    expect(result).toBe('fallback')
  })

  it('returns decoded value when AUTH_PASS_B64 is valid and non-empty', () => {
    const encoded = Buffer.from('MyStr0ngP@ssword!', 'utf8').toString('base64')
    const result = resolveSeedAuthPassword({
      AUTH_PASS_B64: encoded,
    } as unknown as NodeJS.ProcessEnv)
    expect(result).toBe('MyStr0ngP@ssword!')
  })
})
