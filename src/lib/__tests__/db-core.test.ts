// logAuditEvent
// ────────────────────────────────────────────────────────────────────────────
describe('logAuditEvent', () => {
  beforeEach(() => {
    vi.clearAllMocks()
    mockGet.mockReturnValue({ count: 1 })
  })

  it('inserts an audit record with all fields', () => {
    logAuditEvent({
      action: 'user_created',
      actor: 'admin',
      actor_id: 1,
      target_type: 'user',
      target_id: 42,
      detail: { email: 'test@example.com' },
      ip_address: '127.0.0.1',
      user_agent: 'Mozilla/5.0',
    })

    expect(mockRun).toHaveBeenCalledWith(
      'user_created',
      'admin',
      1,
      'user',
      42,
      JSON.stringify({ email: 'test@example.com' }),
      '127.0.0.1',
      'Mozilla/5.0',
    )
  })

  it('inserts null for optional fields when omitted', () => {
    logAuditEvent({ action: 'config_changed', actor: 'system' })

    expect(mockRun).toHaveBeenCalledWith(
      'config_changed',
      'system',
      null,
      null,
      null,
      null,
      null,
      null,
    )
  })

  it('broadcasts audit.security for security-sensitive actions', () => {
    const securityActions = ['login_failed', 'user_created', 'user_deleted', 'password_change']
    for (const action of securityActions) {
      vi.clearAllMocks()
      mockGet.mockReturnValue({ count: 1 })
      logAuditEvent({ action, actor: 'system' })
      expect(mockBroadcast).toHaveBeenCalledWith(
        'audit.security',
        expect.objectContaining({ action }),
      )
    }
  })

  it('does NOT broadcast for non-security actions', () => {
    logAuditEvent({ action: 'config_changed', actor: 'admin' })
    expect(mockBroadcast).not.toHaveBeenCalledWith('audit.security', expect.anything())
  })

  it('includes timestamp in broadcast payload', () => {
    const before = Math.floor(Date.now() / 1000)
    logAuditEvent({ action: 'login_failed', actor: 'hacker' })
    const after = Math.floor(Date.now() / 1000)

    const broadcastCall = mockBroadcast.mock.calls.find(
      (c: any[]) => c[0] === 'audit.security'
    )
    expect(broadcastCall).toBeDefined()
    const payload = broadcastCall![1] as { timestamp: number }
    expect(payload.timestamp).toBeGreaterThanOrEqual(before)
    expect(payload.timestamp).toBeLessThanOrEqual(after)
  })
})

