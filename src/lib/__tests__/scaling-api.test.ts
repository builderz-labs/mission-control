describe('workspace scoping', () => {
  it('policies are scoped to workspace_id', () => {
    const policy1 = { name: 'Autoscaler', workspace_id: 1 }
    const policy2 = { name: 'Autoscaler', workspace_id: 2 }
    // Same name in different workspaces is allowed
    expect(policy1.workspace_id).not.toBe(policy2.workspace_id)
  })

  it('events reference workspace_id for access control', () => {
    const event = { id: 1, workspace_id: 1, status: 'pending' }
    // Route checks: WHERE id = ? AND workspace_id = ?
    expect(event.workspace_id).toBe(1)
  })

  it('delete cascades events within same policy', () => {
    // DELETE FROM scaling_events WHERE policy_id = ?
    // DELETE FROM scaling_policies WHERE id = ? AND workspace_id = ?
    const policyId = 5
    const events = [
      { id: 1, policy_id: 5 },
      { id: 2, policy_id: 5 },
      { id: 3, policy_id: 6 },
    ]
    const cascaded = events.filter(e => e.policy_id === policyId)
    expect(cascaded).toHaveLength(2)
  })
})

