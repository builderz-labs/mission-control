describe('event status transitions', () => {
  it('pending events can be approved', () => {
    const validTransitions = { pending: ['approved', 'rejected'] }
    expect(validTransitions.pending).toContain('approved')
  })

  it('pending events can be rejected', () => {
    const validTransitions = { pending: ['approved', 'rejected'] }
    expect(validTransitions.pending).toContain('rejected')
  })

  it('non-pending events cannot be acted upon', () => {
    const terminalStatuses = ['approved', 'rejected', 'executed', 'failed']
    for (const status of terminalStatuses) {
      expect(status).not.toBe('pending')
    }
  })

  it('scale_down approval requires agentId', () => {
    const event = { event_type: 'scale_down', status: 'pending' }
    const action = { action: 'approve' as const, agentId: undefined }
    // Route should reject: agentId required for scale_down
    expect(event.event_type).toBe('scale_down')
    expect(action.agentId).toBeUndefined()
  })

  it('scale_up approval does not require agentId', () => {
    const event = { event_type: 'scale_up', status: 'pending' }
    const action = { action: 'approve' as const }
    expect(event.event_type).toBe('scale_up')
    expect(action).not.toHaveProperty('agentId')
  })
})

describe('auto-approve behavior', () => {
  it('auto_approve=1 triggers immediate execution after evaluation', () => {
    // When policy has auto_approve=1 and evaluateScaling returns an event,
    // the evaluate route should call executeScaleUp/Down immediately
    const policy = { auto_approve: 1 }
    expect(policy.auto_approve).toBeTruthy()
  })

  it('auto_approve=0 leaves event in pending status', () => {
    const policy = { auto_approve: 0 }
    expect(policy.auto_approve).toBeFalsy()
  })

  it('scale_down auto-approve picks oldest idle agent', () => {
    // Route queries: SELECT id FROM agents WHERE workspace_id=? AND status='idle' ORDER BY updated_at ASC LIMIT 1
    const agents = [
      { id: 10, status: 'idle', updated_at: 100 },
      { id: 11, status: 'idle', updated_at: 200 },
      { id: 12, status: 'busy', updated_at: 50 },
    ]
    const idleAgents = agents.filter(a => a.status === 'idle').sort((a, b) => a.updated_at - b.updated_at)
    expect(idleAgents[0].id).toBe(10)
  })
})

