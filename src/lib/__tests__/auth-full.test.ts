// ── getUserFromRequest ───────────────────────────────────────────────────────
describe('getUserFromRequest', () => {
  const origEnv = { ...process.env }

  beforeEach(() => {
    vi.clearAllMocks()
    process.env = { ...origEnv, API_KEY: '' }
    mockGet.mockReturnValue(undefined)
    // reset plugin hook
    registerAuthResolver(() => null)
  })

  afterEach(() => {
    process.env = { ...origEnv }
  })

  it('returns null when no auth mechanism is present', () => {
    expect(getUserFromRequest(makeRequest())).toBeNull()
  })

  it('authenticates via session cookie', () => {
    const sessionRow = {
      id: 3, username: 'cookie-user', display_name: 'Cookie', role: 'operator' as const,
      provider: 'local' as const, email: null, avatar_url: null, is_approved: 1,
      workspace_id: 1, tenant_id: 1, created_at: 0, updated_at: 0, last_login_at: null,
      session_id: 55,
    }
    // WHY: In getUserFromRequest, the session cookie path fires BEFORE the API key
    // path — so the validateSession DB query (JOIN on user_sessions) is call #1,
    // and resolveActiveApiKey's settings lookup is never reached if session succeeds.
    // The workspace/tenant fallback calls happen inside validateSession's return path.
    mockGet
      .mockReturnValueOnce(sessionRow)           // (1) validateSession JOIN query
      .mockReturnValue({ id: 1, tenant_id: 1 }) // (2+) workspace/tenant fallback

    const req = makeRequest({ cookie: 'mc_session=valid-session-token' })
    const result = getUserFromRequest(req)
    expect(result).not.toBeNull()
    expect(result!.username).toBe('cookie-user')
  })

  it('authenticates via x-api-key header matching API_KEY env var', () => {
    process.env.API_KEY = 'my-secret-api-key'
    // settings DB lookup returns nothing → falls back to env
    mockGet.mockReturnValue(undefined)

    const req = makeRequest({ 'x-api-key': 'my-secret-api-key' })
    const result = getUserFromRequest(req)
    expect(result).not.toBeNull()
    expect(result!.username).toBe('api')
    expect(result!.role).toBe('admin')
  })

  it('authenticates via Authorization Bearer header', () => {
    process.env.API_KEY = 'bearer-key'
    mockGet.mockReturnValue(undefined)

    const req = makeRequest({ authorization: 'Bearer bearer-key' })
    const result = getUserFromRequest(req)
    expect(result).not.toBeNull()
    expect(result!.username).toBe('api')
  })

  it('rejects mismatched API key', () => {
    process.env.API_KEY = 'correct-key'
    mockGet.mockReturnValue(undefined)

    const req = makeRequest({ 'x-api-key': 'wrong-key' })
    const result = getUserFromRequest(req)
    expect(result).toBeNull()
  })

  it('attaches agent_name from x-agent-name header', () => {
    process.env.API_KEY = 'key'
    mockGet.mockReturnValue(undefined)

    const req = makeRequest({ 'x-api-key': 'key', 'x-agent-name': 'ultron' })
    const result = getUserFromRequest(req)
    expect(result).not.toBeNull()
    expect(result!.agent_name).toBe('ultron')
  })

  it('returns null for empty x-agent-name (whitespace)', () => {
    // agent_name should be null when header is blank
    process.env.API_KEY = 'key'
    mockGet.mockReturnValue(undefined)

    const req = makeRequest({ 'x-api-key': 'key', 'x-agent-name': '   ' })
    const result = getUserFromRequest(req)
    expect(result!.agent_name).toBeNull()
  })

  it('uses settings DB override for API key over env var', () => {
    process.env.API_KEY = 'env-key'
    // First call: settings lookup returns DB override key
    mockGet.mockReturnValueOnce({ value: 'db-override-key' })

    const req = makeRequest({ 'x-api-key': 'db-override-key' })
    const result = getUserFromRequest(req)
    expect(result).not.toBeNull()
    expect(result!.username).toBe('api')
  })
})

