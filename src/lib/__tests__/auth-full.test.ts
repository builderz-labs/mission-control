// ── authenticateUser ─────────────────────────────────────────────────────────
describe('authenticateUser', () => {
  beforeEach(() => {
    vi.clearAllMocks()
    // Default: seedAdminUserFromEnv calls COUNT(*) on init — return non-zero to skip seeding
    mockGet.mockReturnValue({ count: 1 })
  })

  it('returns null when user does not exist', () => {
    // First call is COUNT(*) for seed check, second is the actual user lookup
    mockGet.mockReturnValueOnce({ count: 1 }).mockReturnValue(undefined)
    const result = authenticateUser('ghost', 'anypass')
    expect(result).toBeNull()
  })

  it('returns null for non-local provider (prevents password bypass)', () => {
    mockGet.mockReturnValueOnce(makeDbUserRow({ provider: 'google' }))
    const result = authenticateUser('testuser', 'correct-password')
    expect(result).toBeNull()
  })

  it('returns null when user is not approved', () => {
    mockGet.mockReturnValueOnce(makeDbUserRow({ is_approved: 0 }))
    const result = authenticateUser('testuser', 'correct-password')
    expect(result).toBeNull()
  })

  it('returns null when password is wrong', () => {
    mockGet.mockReturnValueOnce(makeDbUserRow())
    // verifyPassword mock returns false for anything except 'hashed:correct-password'
    const result = authenticateUser('testuser', 'wrong-password')
    expect(result).toBeNull()
  })

  it('returns user object on valid credentials', () => {
    mockGet.mockReturnValueOnce(makeDbUserRow())
      .mockReturnValue({ tenant_id: 1 }) // workspace query
    const result = authenticateUser('testuser', 'correct-password')
    expect(result).not.toBeNull()
    expect(result!.username).toBe('testuser')
    expect(result!.role).toBe('operator')
  })

  it('falls back to local provider when provider field is null', () => {
    mockGet.mockReturnValueOnce(makeDbUserRow({ provider: null }))
      .mockReturnValue({ tenant_id: 1 })
    const result = authenticateUser('testuser', 'correct-password')
    expect(result).not.toBeNull()
    expect(result!.provider).toBe('local')
  })

  it('defaults is_approved to 1 when field is null', () => {
    mockGet.mockReturnValueOnce(makeDbUserRow({ is_approved: null }))
      .mockReturnValue({ tenant_id: 1 })
    const result = authenticateUser('testuser', 'correct-password')
    // is_approved null → treated as 1 → should succeed
    expect(result).not.toBeNull()
  })
})

