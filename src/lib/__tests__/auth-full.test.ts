// ── requireRole ───────────────────────────────────────────────────────────────
describe('requireRole', () => {
  beforeEach(() => {
    vi.clearAllMocks()
    // Default: no DB overrides, settings returns nothing
    mockGet.mockReturnValue(undefined)
    delete process.env.API_KEY
    // Reset any resolver from previous tests
    registerAuthResolver(null as any)
  })

  it('returns 401 when no user is authenticated', () => {
    const req = new Request('http://localhost/')
    const result = requireRole(req, 'viewer')
    expect(result.status).toBe(401)
    expect(result.error).toMatch(/Authentication required/i)
  })

  it('returns 403 when authenticated user has insufficient role', () => {
    // WHY: registerAuthResolver is the hook called AFTER env key check fails.
    // It receives (apiKey, agentName) and returns a User or null.
    // Register a resolver that returns a viewer for any key, then require 'operator' → 403.
    registerAuthResolver((_apiKey: string, _agentName: string | null) => ({
      id: 99,
      username: 'viewer-user',
      display_name: 'Viewer',
      role: 'viewer' as const,
      workspace_id: 1,
      tenant_id: 1,
      created_at: 0,
      updated_at: 0,
      last_login_at: null,
      is_approved: 1 as const,
      auth_provider: null,
      agent_name: null,
    }))

    const req = new Request('http://localhost/', { headers: { 'x-api-key': 'some-key' } })
    const result = requireRole(req, 'operator')
    expect(result.status).toBe(403)
    expect(result.error).toMatch(/operator/i)
  })

  it('returns the user when role is met or exceeded', () => {
    // Admin env key → role 'admin', require 'admin' → passes
    process.env.API_KEY = 'admin-key'
    mockGet.mockReturnValue(undefined) // no DB override
    const req = new Request('http://localhost/', { headers: { 'x-api-key': 'admin-key' } })
    const result = requireRole(req, 'admin')
    expect(result.status).toBeUndefined()
    expect(result.user).toBeDefined()
    expect(result.user!.role).toBe('admin')
  })
})

