describe('createSession', () => {
  beforeEach(() => {
    vi.clearAllMocks()
    // workspace_id lookup for user, tenant lookup for workspace
    mockGet.mockReturnValue({ workspace_id: 1, tenant_id: 1 })
  })

  it('returns a hex token and future expiresAt', () => {
    const { token, expiresAt } = createSession(1)
    expect(token).toMatch(/^[0-9a-f]{64}$/)
    expect(expiresAt).toBeGreaterThan(Math.floor(Date.now() / 1000))
  })

  it('expiresAt is approximately 7 days from now', () => {
    const { expiresAt } = createSession(1)
    const sevenDays = 7 * 24 * 60 * 60
    const now = Math.floor(Date.now() / 1000)
    expect(expiresAt).toBeGreaterThanOrEqual(now + sevenDays - 5)
    expect(expiresAt).toBeLessThanOrEqual(now + sevenDays + 5)
  })

  it('calls INSERT on user_sessions and UPDATE on users', () => {
    createSession(42, '10.0.0.1', 'test-agent')
    // Two prepare().run calls: INSERT session + UPDATE last_login + DELETE expired
    expect(mockRun).toHaveBeenCalledTimes(3)
  })

  it('generates a different token each call', () => {
    const { token: t1 } = createSession(1)
    const { token: t2 } = createSession(1)
    expect(t1).not.toBe(t2)
  })
})

