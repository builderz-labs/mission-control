// --- Zod Schemas (replicated from routes for validation testing) ---

const createDebateSchema = z.object({
  topic: z.string().min(1).max(1000),
  participantIds: z.array(z.number().int().positive()).min(2).max(20),
  maxRounds: z.number().int().min(1).max(20).default(3),
  tokenBudget: z.number().int().min(100).max(1_000_000).default(10_000),
})

const deleteDebateSchema = z.object({
  id: z.number().int().positive(),
})

const submitArgumentSchema = z.object({
  agentId: z.number().int().positive(),
  content: z.string().min(1).max(50_000),
  confidence: z.number().min(0).max(1),
})

const voteSchema = z.object({
  agentId: z.number().int().positive(),
  vote: z.enum(['accept', 'reject']),
  reason: z.string().max(5000).optional(),
})

const startDebateSchema = z.object({
  topic: z.string().min(1).max(1000),
  participantIds: z.array(z.number().int().positive()).min(2).max(10),
  maxRounds: z.number().int().min(1).max(20).optional(),
  tokenBudget: z.number().int().min(100).max(1_000_000).optional(),
})

// --- Tests ---

describe('createDebateSchema', () => {
  it('accepts valid debate creation with all fields', () => {
    const result = createDebateSchema.safeParse({
      topic: 'Should we use microservices?',
      participantIds: [1, 2, 3],
      maxRounds: 5,
      tokenBudget: 50_000,
    })
    expect(result.success).toBe(true)
    if (result.success) {
      expect(result.data.topic).toBe('Should we use microservices?')
      expect(result.data.participantIds).toEqual([1, 2, 3])
      expect(result.data.maxRounds).toBe(5)
      expect(result.data.tokenBudget).toBe(50_000)
    }
  })

  it('applies defaults for maxRounds and tokenBudget', () => {
    const result = createDebateSchema.safeParse({
      topic: 'Test topic',
      participantIds: [1, 2],
    })
    expect(result.success).toBe(true)
    if (result.success) {
      expect(result.data.maxRounds).toBe(3)
      expect(result.data.tokenBudget).toBe(10_000)
    }
  })

  it('rejects empty topic', () => {
    const result = createDebateSchema.safeParse({
      topic: '',
      participantIds: [1, 2],
    })
    expect(result.success).toBe(false)
  })

  it('rejects topic exceeding 1000 characters', () => {
    const result = createDebateSchema.safeParse({
      topic: 'x'.repeat(1001),
      participantIds: [1, 2],
    })
    expect(result.success).toBe(false)
  })

  it('rejects fewer than 2 participants', () => {
    const result = createDebateSchema.safeParse({
      topic: 'Test',
      participantIds: [1],
    })
    expect(result.success).toBe(false)
  })

  it('rejects more than 20 participants', () => {
    const ids = Array.from({ length: 21 }, (_, i) => i + 1)
    const result = createDebateSchema.safeParse({
      topic: 'Test',
      participantIds: ids,
    })
    expect(result.success).toBe(false)
  })

  it('rejects non-positive participant IDs', () => {
    const result = createDebateSchema.safeParse({
      topic: 'Test',
      participantIds: [0, 2],
    })
    expect(result.success).toBe(false)
  })

  it('rejects non-integer participant IDs', () => {
    const result = createDebateSchema.safeParse({
      topic: 'Test',
      participantIds: [1.5, 2],
    })
    expect(result.success).toBe(false)
  })

  it('rejects maxRounds exceeding 20', () => {
    const result = createDebateSchema.safeParse({
      topic: 'Test',
      participantIds: [1, 2],
      maxRounds: 21,
    })
    expect(result.success).toBe(false)
  })

  it('rejects maxRounds of 0', () => {
    const result = createDebateSchema.safeParse({
      topic: 'Test',
      participantIds: [1, 2],
      maxRounds: 0,
    })
    expect(result.success).toBe(false)
  })

  it('rejects tokenBudget below 100', () => {
    const result = createDebateSchema.safeParse({
      topic: 'Test',
      participantIds: [1, 2],
      tokenBudget: 50,
    })
    expect(result.success).toBe(false)
  })

  it('rejects tokenBudget exceeding 1,000,000', () => {
    const result = createDebateSchema.safeParse({
      topic: 'Test',
      participantIds: [1, 2],
      tokenBudget: 1_000_001,
    })
    expect(result.success).toBe(false)
  })

  it('rejects missing participantIds', () => {
    const result = createDebateSchema.safeParse({ topic: 'Test' })
    expect(result.success).toBe(false)
  })

  it('accepts exactly 2 participants (minimum)', () => {
    const result = createDebateSchema.safeParse({
      topic: 'Test',
      participantIds: [1, 2],
    })
    expect(result.success).toBe(true)
  })

  it('accepts exactly 20 participants (maximum)', () => {
    const ids = Array.from({ length: 20 }, (_, i) => i + 1)
    const result = createDebateSchema.safeParse({
      topic: 'Test',
      participantIds: ids,
    })
    expect(result.success).toBe(true)
  })
})

