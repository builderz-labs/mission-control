describe('debate phase advancement validation', () => {
  it('valid debate phases are propose, critique, rebut, vote', () => {
    const validPhases = ['propose', 'critique', 'rebut', 'vote']
    for (const phase of validPhases) {
      expect(typeof phase).toBe('string')
    }
    expect(validPhases).toHaveLength(4)
  })

  it('concluded and budget_exhausted are terminal states', () => {
    const terminalStatuses = ['concluded', 'budget_exhausted']
    // These should not allow further advancement
    for (const status of terminalStatuses) {
      expect(status).toBeTruthy()
    }
  })

  it('vote values are accept or reject only', () => {
    const voteEnum = z.enum(['accept', 'reject'])
    expect(voteEnum.safeParse('accept').success).toBe(true)
    expect(voteEnum.safeParse('reject').success).toBe(true)
    expect(voteEnum.safeParse('abstain').success).toBe(false)
    expect(voteEnum.safeParse('maybe').success).toBe(false)
  })
})

describe('query parameter validation', () => {
  it('status filter values match debate statuses', () => {
    const validStatuses = ['pending', 'propose', 'critique', 'rebut', 'vote', 'concluded', 'budget_exhausted']
    for (const s of validStatuses) {
      expect(typeof s).toBe('string')
      expect(s.length).toBeGreaterThan(0)
    }
  })

  it('pagination defaults are reasonable', () => {
    const defaultLimit = 50
    const maxLimit = 100
    const defaultOffset = 0

    expect(defaultLimit).toBeLessThanOrEqual(maxLimit)
    expect(defaultOffset).toBe(0)
    expect(Math.min(parseInt('200', 10), maxLimit)).toBe(100)
    expect(Math.min(parseInt('50', 10), maxLimit)).toBe(50)
  })

  it('round and phase filters are valid query params for arguments', () => {
    const validRounds = [1, 2, 3, 4, 5]
    const validPhases = ['propose', 'critique', 'rebut']

    for (const r of validRounds) {
      expect(Number.isInteger(r)).toBe(true)
      expect(r).toBeGreaterThan(0)
    }
    for (const p of validPhases) {
      expect(typeof p).toBe('string')
    }
  })
})
