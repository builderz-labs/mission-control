def _short_sender(sender: str) -> str:
    """Shorten an email sender to first name or display name for voice."""
    if "<" in sender:
        name = sender.split("<")[0].strip().strip('"')
        return name.split()[0] if name else sender
    return sender.split("@")[0] if "@" in sender else sender


